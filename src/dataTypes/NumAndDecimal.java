package dataTypes;

public class NumAndDecimal {

	public static void main(String[] args) {

		// 1. Variable Declaration
		
		float num1;
		
		// 2. assign value ( Variable Initialization )
		
		num1=100.06f;
		
		// 3. Usage
		
		System.out.println("Value of num1 is "+num1);
		
		
		
		// 1. Variable Declaration
		
		float distance;
		
		// 2. Variable Initialization ( assign value )
		
		distance=480.20f;
		
		// 3. Usage
		
		System.out.println("Distance between Latur to Pune "+distance+" KM");
		
		
		
		
		// data type double
		
		// 1. variable declaration
		
		double weight;
		
		
		// 2. assign the value
		
		weight=10.387569382907629874976d;
		
		// 3. usage
		
		System.out.println("The value of weight is "+weight);


	}

}
